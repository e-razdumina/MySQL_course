DROP DATABASE IF EXISTS shop;
CREATE DATABASE shop;
USE shop;

CREATE TABLE catalogs (
  id SERIAL PRIMARY KEY,
  name VARCHAR(255) COMMENT 'Название раздела',
  UNIQUE unique_name(name(10))
) COMMENT = 'Разделы интернет-магазина';

DROP TABLE IF EXISTS users;
CREATE TABLE users (
  id SERIAL PRIMARY KEY,
  name VARCHAR(255) COMMENT 'Имя покупателя',
  birthday_at DATE COMMENT 'Дата рождения',
  created_at DATETIME DEFAULT CURRENT_TIMESTAMP,
  updated_at DATETIME DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP
) COMMENT = 'Покупатели';

DROP TABLE IF EXISTS products;
CREATE TABLE products (
  id SERIAL PRIMARY KEY,
  name VARCHAR(255) COMMENT 'Название',
  description TEXT COMMENT 'Описание',
  price DECIMAL (11,2) COMMENT 'Цена',
  catalog_id INT UNSIGNED,
  created_at DATETIME DEFAULT CURRENT_TIMESTAMP,
  updated_at DATETIME DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  KEY index_of_catalog_id (catalog_id)
) COMMENT = 'Товарные позиции';

DROP TABLE IF EXISTS logs;
CREATE TABLE logs ( 
created_at DATETIME, 
table_name VARCHAR(255) COMMENT 'Имя таблицы', 
key_id INT COMMENT 'Идентификатор ключа', 
name VARCHAR(255) COMMENT 'Содержимое поля name'
) COMMENT = 'Лог' 
ENGINE=Archive;

DROP TRIGGER IF EXISTS log_products;

DELIMITER //
CREATE TRIGGER insert_log_products AFTER INSERT ON products
FOR EACH ROW BEGIN
  INSERT INTO logs values (NOW(), "products", NEW.id, NEW.name);
END//

DELIMITER ;

DROP TRIGGER IF EXISTS log_catalogs;

DELIMITER //
CREATE TRIGGER insert_log_catalogs AFTER INSERT ON catalogs
FOR EACH ROW BEGIN
  INSERT INTO logs values (NOW(), "catalogs", NEW.id, NEW.name);
END//

DELIMITER ;

DROP TRIGGER IF EXISTS log_users;

DELIMITER //
CREATE TRIGGER insert_log_users AFTER INSERT ON users
FOR EACH ROW BEGIN
  INSERT INTO logs values (NOW(), "users", NEW.id, NEW.name);
END//

DELIMITER ;




